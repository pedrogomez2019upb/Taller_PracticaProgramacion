#Fuerza con la segunda ley de Newton
#Para ello nos toca recolectar la masa y la aceleracion del objeto.
m=float(input("Bienvenido! Vamos a calcular la fuerza de un objeto de acuerdo a la segunda ley de Newton . Por favor ingresa la masa del objeto: "))
a=float(input("Ahora ingresa la aceleración de este mismo: "))
#Planteamos la formula para sacar la fuerza
f=m*a
#Finalmente lo imprimimos
print("El valor de la fuerza es: {}. Gracias por utilizar el programa.".format(f))
