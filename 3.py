#Algoritmo que imprima el cuadrado de un número
#Primero saludamos al cliente y le solicitamos el número que se va a llevar al cuadrado
#Esta variable la vamos a poner como un float para que pueda ser cualquier valor
x=float(input("Bienvenido! Por favor ingresa el número que quieres llevar al cuadrado: "))
#Declaramos otra varialbe que tenga el procedimiento matemático que eleve la variable x al cuadrado
y=x*x
#Finalmente imprimimos la respuesta con la variable "x" y "y"
print ("El número {} al cuadrado da como el resultado de: {}".format(x,y))
