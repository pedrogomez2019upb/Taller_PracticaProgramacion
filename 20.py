#Convierta un valor n en nn y nnn
#Primero hay que recibir el valor n
n=int(input("Bienvenido! Vamos a volver tu número un nn y nnn. Por favor ingresa el valor n: "))
#Planteamos las ecuaciones que lo van a convertir
nn=n*11
nnn=n*111
#Finalmente vamos a imprimir el valor creado
print("Los valores convertidos son :  {}:{}:{} .".format(n,nn,nnn))
