#Area y Perimetro de un circulo.
#Primero nos toca recibir el valor del radio de circulo a evaluar
r=float(input("Bienvenido! Vamos a sacarle el área y perimetro de un circulo sabiendo que pi=3.1416 . Por favor ingresa el valor del radio: "))
#Ahora se hace una variable que saque el area y perimetro del circulo.
a=3.1416*(r*r)
p=2*3.1416*r
#Finalmente se imprime el resultado con un mensaje.
print("El área de un círculo con radio {} es : {} , y el perímetro seria de: {}. Gracias por utilizar el programa.".format(r,a,p))
