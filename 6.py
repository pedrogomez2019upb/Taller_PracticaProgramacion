#Algoritmo que haga la suma ,resta , multiplicación , división y residuo de dos números.
#Primero vamos a solicitar los dos números a revisar.
x=float(input("Bienvenido ! Por favor ingresa el primer valor: "))
y=float(input("Ahora ingresa el segundo valor: "))
#Ahora hacemos cuatro variables que hagan los respectivos procedimientos matemáticos.
a=x+y
b=x-y
c=x*y
d=x/y
#Finalmente imprimimos los resultados con print y las variables.
print("El resultado de la suma ,resta,multiplicación y división de {} y {} es respectivamente: {} {} {} {}".format(x,y,a,b,c,d))
