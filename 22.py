#Segundos a minutos
#Primero vamos a recibir el valor de segundos a convertir
seg=int(input("Bienvenido ! Vamos a convertir segundos a minutos. Por favor ingresa los segundos a convertir: "))
#Planteamos la conversión a minutos
min=seg/60
#Finalmente imprimimos el valor
print("Los segundos {} son iguales a {} minuto/s. Gracias por utilizar el programa.".format(seg,min))
