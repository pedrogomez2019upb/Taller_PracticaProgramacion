#Buscar el mayor de dos números
#Se reciben los dos valores
a=float(input("Bienvenido ! Vamos a ver cual número es mayor que otro. Ingresa el primer valor: "))
b=float(input("Ingresa el segundo valor: "))
#Ponemos condicional if y then
if a>b:
    print("El número {} es mayor que {} . Gracias por utilizar el programa.".format(a,b))
else:
    print("El número {} es mayor que {} . Gracias por utilizar el programa.".format(b,a))
